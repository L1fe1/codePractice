package base.java7.newswitch;

public class NewSwitch {
    public static void main(String[] args) {
        String lang = "Java";
        switch(lang){
            case "Java":
                System.out.println("I like frameworks!");
                break;
            case "Ruby" :
                System.out.println("I like Pabst Blue Ribbon!");
                break;
            case "PHP" :
                System.out.println("I like WordPress!");
                break;
        }
    }
}
